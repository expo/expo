/**
 * Contact date representation.
 */
export type ContactDate = {
  /**
   * Year component of the date. For birthday dates, this field can be omitted to represent a date without a year.
   */
  year?: number;
  /**
   * Month component of the date in format 1-12.
   */
  month: number;
  /**
   * Day component of the date in format 1-31.
   */
  day: number;
};

/**
 * Enum representing non-Gregorian calendar types.
 * @platform ios
 */
export enum NonGregorianCalendar {
  buddhist = 'buddhist',
  chinese = 'chinese',
  coptic = 'coptic',
  ethiopicAmeteMihret = 'ethiopicAmeteMihret',
  ethiopicAmeteAlem = 'ethiopicAmeteAlem',
  hebrew = 'hebrew',
  indian = 'indian',
  islamic = 'islamic',
  islamicCivil = 'islamicCivil',
  japanese = 'japanese',
  persian = 'persian',
  republicOfChina = 'republicOfChina',
}

/**
 * Represents a non-Gregorian birthday date.
 * @platform ios
 */
export type NonGregorianBirthday = {
  /**
   * Year component of the date. For birthday dates, this field can be omitted to represent a date without a year.
   */
  year?: number;
  /**
   * Month component of the date in format 1-12.
   */
  month: number;
  /**
   * Day component of the date in format 1-31.
   */
  day: number;
  /**
   * The calendar system used for the date.
   */
  calendar: NonGregorianCalendar;
};

