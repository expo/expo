  addresses?: (ExistingAddress | NewAddress)[];
  /**
   * Relations associated with the contact. It can be a mix of existing and new relations.
   * Existing relations will be updated; new relations will be added; not present relations will be deleted.
   */
  relations?: (ExistingRelation | NewRelation)[];
  /**
   * URL addresses associated with the contact. It can be a mix of existing and new URL addresses.
   * Existing URL addresses will be updated; new URL addresses will be added; not present urlAddresses will be deleted.
   */
  urlAddresses?: (ExistingUrlAddress | NewUrlAddress)[];
  /**
   * Social profiles associated with the contact. It can be a mix of existing and new social profiles.
   * Existing social profiles will be updated; new social profiles will be added; not present socialProfiles will be deleted.
   * @platform ios
   */
  socialProfiles?: (ExistingSocialProfile | NewSocialProfile)[];
  /**
   * Instant messaging addresses associated with the contact. It can be a mix of existing and new instant messaging addresses.
   * Existing instant messaging addresses will be updated, new instant messaging addresses will be added; not present imAddresses will be deleted.
   * @platform ios
   */
  imAddresses?: (ExistingImAddress | NewImAddress)[];
